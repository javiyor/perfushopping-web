TERMINOS Y CONDICIONES – PROGRAMA DE REFERIDOS (affiliate_v1)
Ultima actualizacion: 2026-04-28

1) Organizador y aceptacion
El Programa de Referidos ("Programa") es ofrecido por Perfushopping ("Perfushopping", "nosotros"). Al registrarte y marcar "Acepto", aceptas estos terminos.

2) Participacion
Todo cliente registrado puede participar como Referidor/Vendedor y compartir su enlace/codigo. Perfushopping puede suspender o dar de baja participantes ante uso indebido o fraude.

3) Atribucion del referido (unico por cliente)
La atribucion es primer referidor (first touch): una vez asignado un referidor a un cliente, no se cambia. La ventana de atribucion del enlace/cookie es de 30 dias.

4) Base de comision y porcentaje
La comision es del 10% sobre el total de productos del pedido (con IVA y con descuentos aplicados), excluyendo el costo de envio. No se generan comisiones por compras mayoristas con transferencia, ni por pedidos cancelados, reembolsados o con contracargo.

5) Acreditacion y liberacion del credito
Las comisiones se calculan con pagos aprobados y confirmados. El credito se libera 7 dias despues de la aprobacion del pago. Perfushopping puede ajustar o revertir creditos por devoluciones, cancelaciones, contracargos, errores o fraude.

6) Credito y uso en compras
La comision se acredita como Credito interno. Puede usarse solo sobre el total de productos (no aplica a envio) y con un limite del 50% del total de productos de cada compra.

7) Retiros en pesos (conversion)
Se puede solicitar retiro a CBU/alias con aprobacion. Minimo de retiro: $20.000 de Credito disponible. El monto a pagar en ARS sera el 50% del Credito retirado. Al solicitar retiro, se debita el 100% del Credito solicitado.

8) Prevencion de fraude, autopromocion y malas practicas
No se permiten referidos propios ni maniobras para generar comisiones artificiales. La comision se bloquea automaticamente si se detectan coincidencias entre comprador y referidor (mismo telefono o misma direccion y localidad). Se prohibe spam o publicidad enganosa.

