POLITICA DE PRIVACIDAD Y COOKIES (version breve)
Ultima actualizacion: 2026-04-28

1) Responsable
Perfushopping es el responsable del tratamiento de los datos en este sitio.

2) Datos que recopilamos
Podemos recopilar y procesar:
- Datos de cuenta: nombre, email, telefono, clave (en forma encriptada).
- Datos de compra y envio: nombre, direccion, localidad, codigo postal, provincia, telefono, email.
- Datos de navegacion basicos: direccion IP, tipo de navegador, paginas visitadas, fecha/hora.
- Datos del Programa de Referidos: codigo/enlace de referido y la relacion entre un referido y un cliente.

3) Para que usamos los datos
Usamos tus datos para:
- Procesar compras, pagos y envios.
- Brindar atencion al cliente.
- Prevenir fraudes y abusos.
- Gestionar el Programa de Referidos (atribucion y creditos).
- Cumplir obligaciones legales y administrativas.

4) Cookies (incluye cookie de referido)
Usamos cookies para el funcionamiento del sitio.
Si llegas con un enlace/codigo de referido, guardamos una cookie de referido para atribuir la compra:
- Duracion: hasta 30 dias.
- Finalidad: atribucion del Programa de Referidos.
Podes desactivar cookies desde tu navegador.

5) Comparticion con terceros
Solo compartimos datos cuando es necesario:
- Procesadores de pago (ej. Mercado Pago).
- Servicios de email (SMTP).
- Proveedores de hosting/infraestructura.
No vendemos tus datos.

6) Seguridad
Aplicamos medidas razonables para proteger la informacion. Ninguna medida es 100% infalible.

7) Conservacion
Conservamos los datos el tiempo necesario para cumplir las finalidades indicadas.

8) Derechos y contacto
